/**
 * TEMPLATE LITERALS
 * 
 *  Forma mais curta de fazer concatenações entre strings
 */

//Exemplo tradicional:
const nome = 'Igor';
const idade = 20;

console.log('Meu nome é ' + nome + ' e tenho ' + idade + ' anos.'); // Muitas concatenações

console.log(`Meu nome é ${nome} e tenho ${idade} anos.`) // forma mais limpa, sem muitas concatenações.